public class ArithmeticOperators {
    public static void main(String[] args) {
        for (int i = 1; i < 11; i++) {
            System.out.println(i);
        }
        System.out.println("------");
        for (int i = 10; i > 0; i--) {
            System.out.println(i);
        }
    }
}
