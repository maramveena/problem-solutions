public class CubeOfNumber {
    /**
     * cube of a number
     * @param n Number
     * @return returns result of a cube of a number
     */
    public static int cubeOfNumber(int n) {
        return n * n * n;
    }

    public static void main(String[] args) {
        System.out.println(cubeOfNumber(3));
    }
}
